
public class Palestrantes extends Palestras {
	
	protected String curriculoPalestrante;
	protected String emailPalestrante;
	protected String telefonePalestrante;
	protected String nomePalestrante;
	
	
	
	
	
	
	
	
	
	public String getNomePalestrante() {
		return nomePalestrante;
	}
	public void setNomePalestrante(String nomePalestrante) {
		this.nomePalestrante = nomePalestrante;
	}
	public String getCurriculoPalestrante() {
		return curriculoPalestrante;
	}
	public void setCurriculoPalestrante(String curriculoPalestrante) {
		this.curriculoPalestrante = curriculoPalestrante;
	}
	public String getEmailPalestrante() {
		return emailPalestrante;
	}
	public void setEmailPalestrante(String emailPalestrante) {
		this.emailPalestrante = emailPalestrante;
	}
	public String getTelefonePalestrante() {
		return telefonePalestrante;
	}
	public void setTelefonePalestrante(String telefonePalestrante) {
		this.telefonePalestrante = telefonePalestrante;
	}

}
